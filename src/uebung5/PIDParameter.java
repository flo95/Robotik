package uebung5;

public class PIDParameter {

	private double Tn;
	private double Kp;
	private double Tv;

	public double getTn() {
		return Tn;
	}

	public void setTn(double tn) {
		Tn = tn;
	}

	public double getKp() {
		return Kp;
	}

	public void setKp(double kp) {
		Kp = kp;
	}

	public double getTv() {
		return Tv;
	}

	public void setTv(double tv) {
		Tv = tv;
	}

}
