package wettkampf.linefollower;

public class LineHolder {

	private int white;
	private int black;

	public LineHolder() {
		
	}

	public void setWhite(int value) {
		white = value;
	}

	public int getWhite() {
		return white;
	}

	public void setBlack(int value) {
		black = value;
	}

	public int getBlack() {
		return black;
	}

}